package com.wxx.entity;

/**
 * 实体类
 * 
 * @author weixx
 * @date 2018-5-27 08:17:40
 *
 */
public class User {

	private int id;
	private String name;
	private String phone;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

}
